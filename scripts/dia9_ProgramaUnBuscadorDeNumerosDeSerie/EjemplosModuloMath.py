import math

# Redondear hacia abajo
resultado_redondeo_inferior = math.floor(88.4)
print(resultado_redondeo_inferior)

# Redondear hacia arriba
resultado_redondeo_superior = math.ceil(88.4)
print(resultado_redondeo_superior)

# Imprimir PI
resultado_numero_pi = math.pi
print(resultado_numero_pi)

# Crear logaritmo
resultado_logaritmo = math.log(25, 5)
print(resultado_logaritmo)
